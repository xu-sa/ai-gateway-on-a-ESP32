void get_8x8_buffer_by_row(char i,uint8_t* buffer,uint16_t size_buffer,uint16_t current_index){
    uint8_t asiic_char=(uint8_t)i;
    uint8_t index=0;
    if(asiic_char>=65&&asiic_char<=90){//lower case
        index=asiic_char - 65;
    }else 
    if(asiic_char>=97&&asiic_char<=122){//upper case
        index=asiic_char - 97;
    }else 
    if(asiic_char>=48&&asiic_char<=57){//digits
        index=asiic_char-48+26;
    }
    else{
        for(int i=0;i<8;i++)buffer[current_index+i]=0;
        return;
    }
    for(int i=0;i<8;i++)buffer[current_index+i]=english_letter_8x8[index][i];
}

void get_8x8_buffer_by_column(char i,uint8_t* buffer,uint16_t size_buffer,uint16_t current_index){
    uint8_t asiic_char=(uint8_t)i;
    uint8_t index=0;
    if(asiic_char>=65&&asiic_char<=90){
        index=(uint8_t)i - 65;
    }else 
    if(asiic_char>=97&&asiic_char<=122){
        index=(uint8_t)i - 97;
    }else 
    if(asiic_char>=48&&asiic_char<=57){
        index=asiic_char-48+26;
    }
    else{
        for(int i=0;i<8;i++)buffer[current_index+i]=0;
        return;
    }
    for(int i=0;i<8;i++)for(int ii=0;ii<8;ii++)buffer[current_index+i]|=((english_letter_8x8[index][ii]>>(7-i))&0x01)<<(7-ii);
}